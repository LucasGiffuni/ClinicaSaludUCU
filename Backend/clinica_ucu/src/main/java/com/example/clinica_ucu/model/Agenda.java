package com.example.clinica_ucu.model;

public class Agenda {

    private String Nro;
    private String Ci;
    private String Fch_Agenda;
    public String getNro() {
        return Nro;
    }
    public void setNro(String nro) {
        Nro = nro;
    }
    public String getCi() {
        return Ci;
    }
    public void setCi(String ci) {
        Ci = ci;
    }
    public String getFch_Agenda() {
        return Fch_Agenda;
    }
    public void setFch_Agenda(String fch_Agenda) {
        Fch_Agenda = fch_Agenda;
    }
    

    
}
